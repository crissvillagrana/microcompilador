   public void setCodigoInter(String llave, String codigo){
       codigoInter.put(llave,codigo);
   }
     
   public String getCodigoInter(){
     String cadena="";
     for(String i : codigoInter.keySet())
        cadena+="key: "+i+"value: "+codigoInter.get(i)+"\n";
        return cadena;
    }

    public String AbrirArchivo(File archivo){
     String documento="";
     try{
          entrada=new FileInputStream(archivo);
          int ascci;
            while((ascci=entrada.read())!=-1){
               char caracter= (char)ascci;
               documento+=caracter;
            }

        }catch(Exception e){}
        return documento;
    }
   
    public String GuardarArchivo(File archivo, String documento){
      String mensaje=null;
      try{
            salida=new FileOutputStream(archivo);
            byte[] bytxt=documento.getBytes();
            salida.write(bytxt);
            mensaje="Codigo Intermedio guardado";
       }catch(Exception e){}
        return mensaje;
    }

