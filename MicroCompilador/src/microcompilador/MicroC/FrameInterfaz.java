    public void inOrden(Nodo n){
      if(n!=null){
            inOrden(n.getDerecho());
            jTextArea2.append(n.getDato()+"\n");
            inOrden(n.getIzquierdo());
        }
    }

    public void preOrden(Nodo n){
      if(n!=null){
            jTextArea1.append(n.getDato()+"\n");
            nPolaca+=jnotacionPolaca.getText()+n.getDato()+" ";
            jnotacionPolaca.setText(jnotacionPolaca.getText()+n.getDato()+" ");
            preOrden(n.getIzquierdo());
            preOrden(n.getDerecho());
        }
    }

    public void postOrden(Nodo n){
      if(n!=null){
            postOrden(n.getDerecho());
            postOrden(n.getIzquierdo());
            jTextArea3.append(n.getDato()+"\n");
        }
    }
    
    public void intermedio(Nodo n){
       if(n!=null){
        intermedio(n.getIzquierdo());
        intermedio(n.getDerecho());
            if(n.getIzquierdo()==null&&n.getDerecho()==null){
                n.setLugar(n.getDato()+"");
                n.setCodigo("");
            }else{
                if((n.getDato().equals("+"))||(n.getDato().equals("*"))){
                    temp++;
                    n.setLugar("T"+temp);
                    String codigol="";
                    String codigolntHash="";
                    Nodo izquierdo=n.getIzquierdo();
                    Nodo derecho=n.getDerecho();

                    codigol=izquierdo.getCodigo()+" "+derecho.getCodigo()+" "+n.getLugar()+" = "+izquierdo.getLugar()+" "+n.getDato()+" "+derecho.getLugar();
                    n.setCodigo(codigol+"\n");

                    codigolntHash=n.getLugar()+" "+izquierdo.getLugar()+n.getDato()+derecho.getLugar();
                    setCodigoInter(Integer.toString(temp),codigolntHash);                 
                    
                }else{
                    if(n.getDato().equals("=")){
                        String codigol="";
                        Nodo izquierdo=n.getIzquierdo();
                        Nodo derecho=n.getDerecho();
                        codigol=derecho.getCodigo()+" "+izquierdo.getLugar()+" = "+" T "+temp;

                        n.setCodigo(codigol+"\n");
                    }
                }
            }
        }
    }
    

